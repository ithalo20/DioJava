public class ExemploFor {
	public static void main(String[] args) {
        for (int carneirinhos = 1; carneirinhos <= 20; carneirinhos++) {
            System.out.println(carneirinhos + " - Carneirinho(s)");
        }
        System.out.println("\nJoãozinho dormiu");
	}
}

