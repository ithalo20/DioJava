public class OperadoresCondicoes {
    public static void main(String[] args) {
        int a, b;
        String resultado;
        a = 5;
        b = 4;

        resultado = "";

        resultado = (a == b ? "Verdadeiro" : "Falso"); //? = true e : = false

        System.out.println(resultado);
        
    }
}

// Nesse exemplo são condições com boolean true e false

// ? = true
// : = false

// <Expressão Condicional>`` ?``<Caso condição seja true>``: ``<Caso condição seja false>